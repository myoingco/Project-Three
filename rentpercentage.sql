CREATE TABLE RentPercentage (
    Index INTEGER PRIMARY KEY,
    State VARCHAR(255),
	Year INTEGER,
    Population FLOAT,
    Studio FLOAT,
    One_Bedroom FLOAT,
    Two_Bedroom FLOAT,
    Three_Bedroom FLOAT,
    Four_Bedroom FLOAT,
    Studio_Percent_Change FLOAT,
    One_Bedroom_Percent_Change FLOAT,
    Two_Bedroom_Percent_Change FLOAT,
    Three_Bedroom_Percent_Change FLOAT,
    Four_Bedroom_Percent_Change FLOAT
);

